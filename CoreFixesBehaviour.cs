        private static void ShopRestoration() {
            foreach (var val in MedleyTable.entityMedleyTable.dictionary.Values) {
                int newSoundMedal = -1;

                foreach (ShopData sd in autogenerated_116) {
                    if (sd.id == val.musicID[0]) {
                        newSoundMedal = sd.cost;
                    }
                }

                if (newSoundMedal > 0) {
                    val.soundMedal = newSoundMedal;
                    val.secretMusic = true;
                }
            }
        }

        public static string Md5(string filename) {
            if (!File.Exists(filename)) {
                return null;
            }
            using (var md5 = MD5.Create()) {
                using (var stream = File.OpenRead(filename)) {
                    var hash = md5.ComputeHash(stream);
                    return BitConverter.ToString(hash).Replace("-", "").ToLowerInvariant();
                }
            }
        }

